CREATE TABLE EXAMS (
    EXAM_ID INT PRIMARY KEY,
    STUDENT_ID INT,
    SUBJECT VARCHAR(50),
    MARKS INT,
    FOREIGN KEY (STUDENT_ID) REFERENCES STUDENTS(STUDENT_ID)
